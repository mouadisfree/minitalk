#include "ft_printf.h"

int	ft_putunsigned(unsigned int n)
{
	int	i;

	i = 0;
	if (n >= 10)
		i += ft_putunsigned(n / 10);
	i += write(1, &"0123456789"[n % 10], 1);
	return (i);
}
